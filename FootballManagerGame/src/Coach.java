class Coach {
    private String name;
    private String[] strategies;

    public Coach(String name) {
        this.name = name;
        this.strategies = strategies;
    }

    public String getName() {
        return name;
    }

    public String[] getStrategies() {
        return strategies;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setStrategies(String[] strategies) {
        this.strategies = strategies;
    }
}